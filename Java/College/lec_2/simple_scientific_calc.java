public class simple_scientific_calc {
    public static void main(String args[]) {
        System.out.println("Sine Inverse = " + Math.asin(1));
        System.out.println("Cube Root = " + Math.cbrt(8));
        System.out.println("Exponential = " + Math.exp(25));
        System.out.println("Logerithmic = " + Math.log(1));
        System.out.println("Random Number = " + Math.random());
        System.out.println("Number raise to Number = " + Math.pow(2, 4));
        System.out.println("Cosine Inverse = " + Math.acos(0.5));
        System.out.println("Tangent Inverse = " + Math.atan(1));
        System.out.println("Sine Inverse = " + Math.min(1, 2));
        System.out.println("Sine Inverse = " + Math.max(1, 5));
    }
}
